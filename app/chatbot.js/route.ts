  "",
  "    const inputWrap = el(\"div\", \"input\");",
  "    const input = document.createElement(\"input\");",
  "    input.id = ids.input;",
  "    input.type = \"text\";",
  "    input.placeholder = \"Type or speak a message\";",
  "",
