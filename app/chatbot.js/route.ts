  "    }",
  "",
  "    const recognition = new SpeechRecognition();",
  "    recognition.lang = \"en-US\";",
  "    recognition.interimResults = true;",
  "    recognition.maxAlternatives = 1;",
  "    recognition.continuous = false;",
  "    state.recognition = recognition;",
  "",
  "    recognition.onstart = function() {",
  "      setVoiceListening(true);",
  "    };",
  "",
  "    recognition.onresult = function(event) {",
  "      let interim = \"\";",
  "      let final = \"\";",
  "      for (let i = event.resultIndex; i < event.results.length; i++) {",
  "        const transcript = event.results[i][0].transcript;",
  "        if (event.results[i].isFinal) { final += transcript; }",
  "        else { interim += transcript; }",
  "      }",
  "      const input = document.getElementById(ids.input);",
  "      if (input) {",
  "        input.value = final || interim;",
  "      }",
  "      if (final) {",
  "        setVoiceStatus(\"Got it!\", true);",
  "        setTimeout(() => setVoiceStatus(\"\", false), 1000);",
  "      }",
  "    };",
  "",
  "    recognition.onerror = function(event) {",
  "      setVoiceListening(false);",
  "      let msg = \"Voice error\";",
  "      if (event.error === \"not-allowed\" || event.error === \"denied\") { msg = \"Microphone access denied\"; }",
  "      else if (event.error === \"no-speech\") { msg = \"No speech detected\"; }",
  "      else if (event.error === \"network\") { msg = \"Network error\"; }",
  "      setVoiceStatus(msg, true);",
  "      setTimeout(() => setVoiceStatus(\"\", false), 3000);",
  "    };",
  "",
